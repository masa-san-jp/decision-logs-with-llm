# 自社提供データ 要件定義書（親会社・子会社対応版）

## 1. 概要
本ドキュメントは、補助金・助成金申請支援マルチエージェントシステムにおいて、適格性判定（エージェント2）および書類作成（エージェント4）を正確に機能させるために、自社側で整備・提供する必要があるデータ項目を定義する。
親会社および複数の子会社が存在するグループ企業であることを前提とし、各法人の基本情報、財務情報、およびプロジェクト（申請事業）情報を構造化して管理する。

## 2. データ構造の基本方針
補助金の要件判定では、「申請法人単体の規模」だけでなく、「親会社の規模や出資比率（みなし大企業判定）」が厳しく問われる。そのため、データは以下の3層構造（リレーショナル構造）で整備する。

1. **グループ統括データ (Group Level)**: 親会社と子会社の関係性（出資比率など）。
2. **法人基本・財務データ (Entity Level)**: 各法人（親会社・子会社それぞれ）の基本スペック。
3. **プロジェクトデータ (Project Level)**: どの法人が、何をしたいかという事業計画の種。

---

## 3. 必要なデータ項目一覧

### 3.1. グループ統括データ（資本関係・ツリー構造）
*AIが「みなし大企業の要件」や「複数社連携枠」の判定を行うために必須のデータ。*

* **グループ名**
* **法人間の資本関係リスト**
  * 親法人ID
  * 子法人ID
  * 出資比率（%）
  * 役員の兼任状況（親会社の役員が子会社の役員の過半数を占めているか等 boolean）

### 3.2. 法人基本・財務データ（親会社・子会社ごとに作成）
*各法人の適格性（要件クリア）を自動判定するために必須の定量データ。最低でも直近2〜3期分の財務データが必要。*

* **基本情報 (Basic Info)**
  * 法人ID / 法人番号 (13桁)
  * 法人名
  * 本店所在地（都道府県・市区町村：※自治体独自補助金の判定に必須）
  * 設立年月日
  * 主たる業種（日本標準産業分類コード：大分類・中分類）
  * 資本金（円）
  * 常時使用する従業員数（役員・パートを除く）
* **財務・労務情報 (Financial & Labor Info) ※直近3期分**
  * 決算年月
  * 売上高
  * 営業利益 / 経常利益
  * 減価償却費
  * 人件費（給与総額）
  * 事業場内最低賃金（円：※賃上げ加点要件に必須）
* **認証・前提資格 (Certifications)**
  * gBizIDプライムアカウントの有無 (boolean)
  * SECURITY ACTION宣言（一つ星・二つ星）の有無
  * 過去の主要な補助金受給履歴（制度名、交付決定日：※重複申請・インターバル制限の判定に必須）

### 3.3. プロジェクトデータ（事業アイデア・投資計画）
*AI（エージェント4）が事業計画書を執筆するための材料となる定性・定量データ。*

* **プロジェクト基本情報**
  * プロジェクトID
  * 申請主体となる法人ID（どの子会社が実施するか、あるいは親会社か）
  * 実施予定場所（自社内、工場、別拠点など）
* **投資・調達内容 (Investment Plan)**
  * 導入予定の設備/システム名（例: 「クラウド型生産管理システム」「〇〇用マシニングセンタ」）
  * 概算費用（総額、および「ソフトウェア費」「機械装置費」などの経費区分）
  * 導入予定時期（〇年〇月頃）
* **事業の背景と目的 (Business Context) ※フリーテキスト可**
  * 現状の自社の強み（既存の顧客基盤、独自技術など）
  * 抱えている課題（人手不足、システムの老朽化、歩留まりの悪化など）
  * 本プロジェクトによる解決策と期待される効果（定性・定量）

