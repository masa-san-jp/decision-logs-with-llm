## 4. Claude Codeへの実装指示（Pydanticモデル定義の要件）

システム開発時、上記データをAIエージェント間で正確に受け渡しするため、Pythonの `Pydantic` を用いて厳密な型定義を行うこと。

**実装すべき主要クラス（例）:**
1. `EntityProfile`: 各法人の基本・財務情報を格納。`is_sme()`（中小企業かどうかの判定）や `check_deemed_large_enterprise()`（みなし大企業判定）のメソッドを持たせる。
2. `GroupStructure`: `EntityProfile` のリストと出資比率を保持し、グループ全体のツリーを管理する。
3. `ProjectIdea`: 特定の `EntityProfile` に紐づき、投資内容や課題のテキストデータを保持する。

## 5. データ整備における自社側の運用アクション
本システムを稼働させるため、自社の経営企画部または管理部門は以下の運用を行う必要がある。
1. 上記 `3.2` の財務データを決算のたびに（年1回）所定のデータベースまたはJSON/CSV形式で更新する。
2. 現場から上がってくる設備投資やシステム導入の稟議・アイデアを、都度 `3.3` のプロジェクトデータとしてシステムに登録（インプット）する。
